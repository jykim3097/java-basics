
public class _5_OperatorEx04 {
	public static void main(String[] args) {
		//Day02
		
		int a = 6;
		int b = 5;
		
		a += 3; // a = a + 3, 9
		b =+ 3; // b = +3
		
		System.out.println("a의 값은 " + a);
		System.out.println("b의 값은 " + b);
	
		a -= 4; // 9-4 = 5
		a *= 10; // 5*10= 50
		a /= 5; // 50/5=10
		a %= 2; // 10%2=0
		
		System.out.println("a의 값은 " + a);
	}
}
